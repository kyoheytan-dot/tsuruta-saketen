// カテゴリ表示名
// ???????????????????????????????
const CAT_LABELS = {
  'original-goek':'五石橋（オリジナル）',
  'original-non':'飲ん日和（オリジナル）',
  'kokubun':'国分酒造',
  'hamada':'濱田酒造',
  'shirogane':'白金酒造',
  'others':'その他',
  'limited':'鹿児島限定',
  'premier':'プレミア焼酎',
};


// ???????????????????????????????
// 管理一覧描画
// ???????????????????????????????
function renderAdminList(){
  const cats = Object.keys(CAT_LABELS);
  let h='';

  cats.forEach(cat=>{
    const items = prods.filter(p=>p.cat===cat);
    if(!items.length) return;

    h+=`<div class="admin-gl">${CAT_LABELS[cat]}</div>`;

    items.forEach(p=>{
      const nm = p.sizes && p.sizes.length
        ? `${p.name} / ${p.sizes.map(x=>x.s).join('・')}`
        : p.name;

      h+=`<div class="admin-item">
        <div class="admin-iinfo">
          <div class="admin-iname">${nm}</div>
          ${p.kura?`<div class="admin-isub">${p.kura}</div>`:''}
        </div>

        ${p.sizes && p.sizes.length
          ? `<div class="admin-iprice">&yen;${p.sizes[0].p.toLocaleString('ja-JP')}</div>`
          : ''}

        <div class="admin-ibtns">
          <button class="a-btn ${p.inStock?'stock':'soldout'}"
            onclick="toggleStock(${p.id})">
            ${p.inStock?'在庫◎':'完売'}
          </button>

          <button class="a-btn edit"
            onclick="openEdit(${p.id})">
            編集
          </button>

          <button class="a-btn del"
            onclick="delProd(${p.id})">
            削除
          </button>
        </div>
      </div>`;
    });
  });

  const el = document.getElementById('admin-list');
  if(el) el.innerHTML = h;
}


// ???????????????????????????????
// 在庫トグル
// ???????????????????????????????
function toggleStock(id){
  const p = prods.find(x=>x.id===id);
  if(p){
    p.inStock = !p.inStock;
    save();
    rerender();
  }
}


// ???????????????????????????????
// 削除
// ???????????????????????????????
function delProd(id){
  if(!confirm('この商品を削除しますか？')) return;

  prods = prods.filter(p=>p.id!==id);
  save();
  rerender();
}


// ???????????????????????????????
// 編集画面オープン
// ???????????????????????????????
function openEdit(id){
  editingId = id;

  const p = id ? prods.find(x=>x.id===id) : null;

  document.getElementById('edit-title').textContent =
    id ? '商品を編集' : '商品を追加';

  document.getElementById('eg').value = p ? p.cat : '';
  document.getElementById('en').value = p ? p.name : '';
  document.getElementById('ek').value = p ? p.kura : '';
  document.getElementById('edesc').value = p ? p.desc : '';
  document.getElementById('eimg').value = p ? p.img : '';
  document.getElementById('ebadge').value =
    p ? (p.badges||[]).join(',') : '';
  document.getElementById('ei').checked =
    p ? p.inStock : true;

  const sz = p ? (p.sizes||[]) : [];

  document.getElementById('es1').value = sz[0]?.s || '';
  document.getElementById('ep1').value = sz[0]?.p || '';
  document.getElementById('es2').value = sz[1]?.s || '';
  document.getElementById('ep2').value = sz[1]?.p || '';
  document.getElementById('es3').value = sz[2]?.s || '';
  document.getElementById('ep3').value = sz[2]?.p || '';

  document.getElementById('edit-overlay').classList.add('show');
}


// ???????????????????????????????
// 編集閉じる
// ???????????????????????????????
function closeEdit(){
  document.getElementById('edit-overlay').classList.remove('show');
}


// ???????????????????????????????
// 保存
// ???????????????????????????????
function saveProd(){
  const cat = document.getElementById('eg').value.trim();
  const name = document.getElementById('en').value.trim();

  if(!cat){ alert('カテゴリを入力してください'); return; }
  if(!name){ alert('商品名を入力してください'); return; }

  const sizes = [];

  [
    [es1.value, ep1.value],
    [es2.value, ep2.value],
    [es3.value, ep3.value]
  ].forEach(([s,p])=>{
    if(s && p){
      sizes.push({ s:s.trim(), p:parseInt(p) });
    }
  });

  const badges = document.getElementById('ebadge')
    .value.split(',')
    .map(x=>x.trim())
    .filter(Boolean);

  const data = {
    cat,
    name,
    kura: ek.value.trim(),
    desc: edesc.value.trim(),
    img: eimg.value.trim(),
    sizes,
    badges,
    inStock: ei.checked,
  };

  if(editingId){
    const p = prods.find(x=>x.id===editingId);
    if(p) Object.assign(p,data);
  } else {
    data.id = prods.length
      ? Math.max(...prods.map(p=>p.id)) + 1
      : 1;
    prods.push(data);
  }

  save();
  rerender();
  closeEdit();
}


// ???????????????????????????????
// 再描画（超重要）
// ???????????????????????????????
function rerender(){
  if(window.renderAll) renderAll();
  if(window.renderForm) renderForm();
  if(window.renderAdminList) renderAdminList();
}
